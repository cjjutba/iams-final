"""
Attendance Prediction Model

Stores weekly attendance predictions generated by the analytics engine.
Used to identify at-risk students before problems escalate.
"""

import enum
import uuid
from datetime import datetime, timezone

from sqlalchemy import Column, Date, DateTime, Float, ForeignKey, String
from sqlalchemy import Enum as SQLEnum
from sqlalchemy.dialects.postgresql import UUID
from sqlalchemy.orm import relationship

from app.database import Base


class RiskLevel(enum.StrEnum):
    """Risk level for attendance predictions."""

    LOW = "low"
    MODERATE = "moderate"
    HIGH = "high"
    CRITICAL = "critical"


class AttendancePrediction(Base):
    """
    Attendance prediction model

    Stores per-student, per-schedule weekly attendance predictions.

    Attributes:
        id: UUID primary key
        student_id: Foreign key to student user
        schedule_id: Foreign key to schedule
        week_start: Start date of the prediction week (Monday)
        predicted_rate: Predicted attendance rate for the week (0-100)
        trend: Trend direction (improving, stable, declining)
        risk_level: Assessed risk level
        actual_rate: Actual attendance rate (filled in after the week ends)
        created_at: When the prediction was generated
    """

    __tablename__ = "attendance_predictions"

    # Primary key
    id = Column(UUID(as_uuid=True), primary_key=True, default=uuid.uuid4)

    # Foreign keys
    student_id = Column(UUID(as_uuid=True), ForeignKey("users.id", ondelete="CASCADE"), nullable=False, index=True)
    schedule_id = Column(UUID(as_uuid=True), ForeignKey("schedules.id", ondelete="CASCADE"), nullable=False, index=True)

    # Prediction data
    week_start = Column(Date, nullable=False, index=True)
    predicted_rate = Column(Float, nullable=False)  # 0-100
    trend = Column(String(20), nullable=False)  # improving, stable, declining
    risk_level = Column(SQLEnum(RiskLevel), nullable=False, index=True)

    # Actual outcome (backfilled)
    actual_rate = Column(Float, nullable=True)  # 0-100, filled after week ends

    # Timestamps
    created_at = Column(DateTime, default=lambda: datetime.now(timezone.utc), nullable=False)

    # Relationships
    student = relationship("User", foreign_keys=[student_id])
    schedule = relationship("Schedule", foreign_keys=[schedule_id])

    def __repr__(self):
        return (
            f"<AttendancePrediction(id={self.id}, student_id={self.student_id}, "
            f"risk={self.risk_level}, predicted={self.predicted_rate})>"
        )
